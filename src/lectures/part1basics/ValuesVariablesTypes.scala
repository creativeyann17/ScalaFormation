package lectures.part1basics

object ValuesVariablesTypes extends App {

  // val is immutable/const/final
  val x: Int = 42
  println(x)

  val aStr: String = "hello"
  val aBool: Boolean = true
  val aChar: Char = 'c'
  val aInt: Int = 5;
  val aShort: Short = 42
  val aLong: Long = 99999999999L
  val aFloat: Float = 2.0f
  val aDouble: Double = 0.1

  // variables
  var aVar: Int = 4
  aVar = 5

}
